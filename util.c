#include <stdio.h>
#include <stdlib.h>
#include "util.h"

void printhex(FILE *fp, const uint8_t *a, int len)
{
	int i;

	for (i = 0; i < len; i++) {
		fprintf(fp, "%.2x", a[i]);
		if (i < len - 1)
			fprintf(fp, ":");
	}
}
